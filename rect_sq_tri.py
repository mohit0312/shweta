def arearectangle(length,breadth):
    '''
       objective : to find the area of rectangle
       input parameters :
                    length of rectangle
                    breadth of rectangle
       return value: area of rectangle
    '''
    #approach: multiply length and breadth of rectangle
    area = length * breadth
    return area
